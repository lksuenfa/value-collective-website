				Value Collective argues that the market’s dominance in the modern economy has displaced all other forms of value and value creation: social value, cultural value, aesthetic value, civic value, ecological value, spiritual value, etc. The result is a monoculture in what might otherwise be a diverse garden of meaning, values, and systems for achieving them. Value Collective aims to demonstrate how an economy can be performed beyond standard market logic.
				<br>
				<br>


				The collective’s home base is currently located at La-Cité-Hospitalières in Montreal, Canada, a former convent purchased by the City of Montreal in 2017. Since 2021, the historic religious complex has begun a transitory phase : an experimental process in which occupants gradually determine the long-term vocation of the building. VC is proud to be part of and contribute to the project Hospitalières-en-transition. To learn more, visit cdhentransition.com.
				<br>
				<br>


				Practically speaking, Value Collective is the host of a residency program aimed at supporting those who wish to dedicate time to work that is valuable but not profitable. Throughout their residency, participants are offered free space, shared resources and other forms of support, and are invited to participate in building community and support networks throughout the cohort. Meanwhile, Value Collective is also exploring systems of exchange that decenter money, in the hopes of bringing greater value to te. and is pursuing research leads around the topic of measuring non-financial value.

