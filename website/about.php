				Value Collective’s mission is to design support structures for intrinsically-motivated people to develop their projects and ideas away from financial pressures. We argue that the dominant role of financial value in today’s society marginalizes other forms of value and value creation: social value, cultural value, aesthetic value, civic value, ecological value, spiritual value, etc. The result is a monoculture in what might otherwise be a diverse garden of meaning, values, and systems for achieving them. Value Collective aims to demonstrate how an economy can be performed beyond standard market logic.
